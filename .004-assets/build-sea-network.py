def norm_lng(x):
    # searoute 会输出 >180/<-180 的连续经度（如太平洋航线 LA=242°）；归一到
    # [-180,180]，跨越反子午线时前端的 |Δlng|>180 断开逻辑就能正确处理。
    return ((float(x) + 180.0) % 360.0) - 180.0


def round_pts(coords):
    return [[round(norm_lng(x), 3), round(float(y), 3)] for x, y in coords]


def main():
    lanes_out = []
    used = set()
    for a, b in LANES:
        if a not in PORTS or b not in PORTS:
            print("skip unknown port:", a, b)
            continue
        try:
            r = sr.searoute(PORTS[a], PORTS[b])
            pts = round_pts(r["geometry"]["coordinates"])
        except Exception as e:  # noqa: BLE001
            print("searoute failed:", a, "->", b, e)
            continue
        if len(pts) < 2:
            continue
        lanes_out.append({"from": a, "to": b, "pts": pts})
        used.add(a); used.add(b)

    ports_out = {k: [round(PORTS[k][0], 3), round(PORTS[k][1], 3)] for k in used}
    out = {
        "meta": {
            "ports": len(ports_out),
            "lanes": len(lanes_out),
            "source": "searoute / Eurostat marine network — curated major liner lanes",
        },
        "ports": ports_out,
        "lanes": lanes_out,
    }
    with open(OUT, "w", encoding="utf-8") as f:
        json.dump(out, f, ensure_ascii=False, separators=(",", ":"))

    size = os.path.getsize(OUT)
    print(f"ports={len(ports_out)}  lanes={len(lanes_out)}  -> {OUT}  ({size/1024:.0f} KB)")


if __name__ == "__main__":
    main()
